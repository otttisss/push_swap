#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*p;

	p = lst;
	while (p != NULL)
	{
		if (f)
		{
			(*f)(p->content);
		}
		p = p->next;
	}
}
