#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		count;

	count = 0;
	if (s && fd)
	{
		while (s[count])
		{
			write(fd, &s[count], 1);
			count++;
		}
		write(fd, "\n", 1);
	}
}
