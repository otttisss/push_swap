char	*ft_strrchr(const char *str, int ch)
{
	char	*s;
	int		i;

	i = 0;
	s = (char *) str;
	while (*s)
	{
		i++;
		s++;
	}
	while (i >= 0)
	{
		if (*s == (char)ch)
			return (s);
		i--;
		s--;
	}
	return (0);
}
